APEX_SIMILARITY_THRESHOLD = 0.80


def validate_apex_submission(data):
    """Check all required fields are present and meet minimums. Returns list of errors."""
    errors = []

    # Check required fields
    for field in APEX_REQUIRED_FIELDS:
        val = data.get(field, '').strip() if isinstance(data.get(field), str) else ''
        if not val:
            errors.append(f"Missing required field: {field.replace('_', ' ').title()}")

    if errors:
        return errors  # Don't continue scoring if fields missing

    # Check minimums
    synopsis_words = len(data.get('synopsis', '').split())
    if synopsis_words < APEX_MIN_SYNOPSIS_WORDS:
        errors.append(f"Synopsis too short: {synopsis_words} words (minimum {APEX_MIN_SYNOPSIS_WORDS})")

    ch1_words = len(data.get('chapter_1', '').split())
    if ch1_words < APEX_MIN_CHAPTER_WORDS:
        errors.append(f"Chapter 1 too short: {ch1_words} words (minimum {APEX_MIN_CHAPTER_WORDS})")

    ch2_words = len(data.get('chapter_2', '').split())
    if ch2_words < APEX_MIN_CHAPTER_WORDS:
        errors.append(f"Chapter 2 too short: {ch2_words} words (minimum {APEX_MIN_CHAPTER_WORDS})")

    outline_points = len([l for l in data.get('outline', '').split('\n') if l.strip()])
    if outline_points < APEX_MIN_OUTLINE_POINTS:
        errors.append(f"Outline too short: {outline_points} points (minimum {APEX_MIN_OUTLINE_POINTS})")

    return errors


def score_apex_submission(data, db_session):
    """Score an Apex submission 0-100 with detailed breakdown."""
    scores = {}
    flags = []

    ch1 = data.get('chapter_1', '')
    ch2 = data.get('chapter_2', '')
    full_text = ch1 + '\n\n' + ch2
    synopsis = data.get('synopsis', '')
    outline = data.get('outline', '')
    logline = data.get('logline', '')

    # A) Hook & Concept (15 points)
    hook_score = 15
    first_sentence = full_text.split('.')[0].lower() if full_text else ''
    weak_hooks = ['my name is', 'i woke up', 'it was a', 'once upon a time',
                  'this is', 'let me', 'hi my name', 'hello']
    for wh in weak_hooks:
        if first_sentence.startswith(wh):
            hook_score -= 5
            flags.append('Weak opening hook detected')
            break

    if not logline or len(logline.split()) < 8:
        hook_score -= 5
        flags.append('Logline too short or missing')

    conflict_words = ['but', 'however', 'must', 'against', 'conflict', 'struggle',
                      'fight', 'challenge', 'threat', 'enemy', 'rival']
    if not any(cw in logline.lower() for cw in conflict_words):
        hook_score -= 3
        flags.append('No clear conflict in logline')

    scores['hook_concept'] = max(0, hook_score)

    # B) Structure & Plot (20 points)
    structure_score = 20
    outline_lines = [l.strip() for l in outline.split('\n') if l.strip()]

    if len(outline_lines) < 10:
        structure_score -= 8
        flags.append('Outline has fewer than 10 points')
    elif len(outline_lines) < 15:
        structure_score -= 3

    protagonist_words = ['protagonist', 'hero', 'main character', 'she', 'he',
                         'they', 'i ', 'me ', 'my ']
    if not any(pw in full_text.lower()[:500] for pw in protagonist_words):
        structure_score -= 4
        flags.append('No clear protagonist in first 500 words')

    stakes_words = ['must', 'need', 'or else', 'lose', 'die', 'save', 'risk', 'danger']
    if not any(sw in full_text.lower() for sw in stakes_words):
        structure_score -= 5
        flags.append('No clear stakes detected')

    scores['structure_plot'] = max(0, structure_score)

    # C) Writing Quality (20 points)
    quality_score = 20
    sentences = re.split(r'[.!?]+', full_text)
    sentences = [s.strip() for s in sentences if s.strip()]
    paragraphs = [p.strip() for p in full_text.split('\n\n') if p.strip()]

    # Professional formatting
    long_paras = [p for p in paragraphs if len(p.split()) > 200]
    if len(long_paras) > 3:
        quality_score -= 4
        flags.append(f'{len(long_paras)} paragraphs exceed 200 words')

    # Sentence variation
    if len(sentences) > 10:
        lengths = [len(s.split()) for s in sentences]
        short = sum(1 for l in lengths if l < 5)
        if short > len(sentences) * 0.5:
            quality_score -= 3
            flags.append('Too many choppy sentences')

    # Spam formatting (all caps, excessive punctuation)
    caps_words = re.findall(r'\b[A-Z]{4,}\b', full_text)
    if len(caps_words) > 15:
        quality_score -= 4
        flags.append('Excessive ALL-CAPS formatting')

    excessive_punct = re.findall(r'[!?]{3,}', full_text)
    if len(excessive_punct) > 5:
        quality_score -= 3
        flags.append('Excessive punctuation (!!!, ???)')

    # Slang check
    slang_words = ['lol', 'omg', 'bruh', 'smh', 'ngl', 'tbh', 'fr fr', 'lowkey']
    slang_count = sum(full_text.lower().count(sw) for sw in slang_words)
    if slang_count > 5:
        quality_score -= 3
        flags.append('Excessive internet slang')

    scores['writing_quality'] = max(0, quality_score)

    # D) Consistency (10 points)
    consistency_score = 10

    # POV consistency — check if mixing first and third person
    first_person = len(re.findall(r'\bI\b', full_text[:2000]))
    third_person = len(re.findall(r'\b(he|she|they)\b', full_text[:2000], re.I))
    if first_person > 10 and third_person > 10:
        consistency_score -= 5
        flags.append('Mixed POV detected (first and third person)')

    scores['consistency'] = max(0, consistency_score)

    # E) Originality Check (20 points)
    originality_score = 20
    from webapp.models import ApexSubmission

    existing = db_session.query(ApexSubmission).filter(
        ApexSubmission.status.in_(['pending_review', 'signed']),
    ).all()

    similarity_warning = False
    max_similarity = 0.0
    for ex in existing:
        if ex.chapter_1:
            sim = fuzz.token_sort_ratio(full_text[:3000], ex.chapter_1[:3000]) / 100.0
            if sim > max_similarity:
                max_similarity = sim
            if sim >= APEX_SIMILARITY_THRESHOLD:
                originality_score = 0
                similarity_warning = True
                flags.append(f'HIGH SIMILARITY ({sim:.0%}) with existing submission #{ex.id}')
                break

    if not similarity_warning and max_similarity > 0.5:
        originality_score -= int((max_similarity - 0.5) * 20)
        flags.append(f'Moderate similarity ({max_similarity:.0%}) detected')

    scores['originality'] = max(0, originality_score)

    # F) Engagement Indicators (10 points)
    engagement_score = 10
    dialogue_count = full_text.count('"') + full_text.count('\u201c')
    if dialogue_count < 6:
        engagement_score -= 4
        flags.append('Very little dialogue')

    scene_breaks = full_text.count('***') + full_text.count('---') + full_text.count('* * *')
    if scene_breaks == 0 and len(full_text.split()) > 3000:
        engagement_score -= 2
        flags.append('No scene breaks detected in long text')

    emotional_words = ['heart', 'tears', 'laugh', 'smile', 'cry', 'angry', 'fear',
                       'love', 'pain', 'joy', 'hope', 'despair', 'tremble']
    emotion_count = sum(full_text.lower().count(ew) for ew in emotional_words)
    if emotion_count < 3:
        engagement_score -= 3
        flags.append('Low emotional engagement')

    scores['engagement'] = max(0, engagement_score)

    # G) Compliance & Safety (5 points)
    compliance_score = 5
    prohibited = ['kill yourself', 'suicide instructions', 'how to make a bomb',
                  'child abuse', 'cp ', 'underage sex']
    for p in prohibited:
        if p in full_text.lower():
            compliance_score = 0
            flags.append(f'PROHIBITED CONTENT DETECTED')
            break

    scores['compliance'] = compliance_score

    # Total
    total = sum(scores.values())

    # Determine strengths and weaknesses
    strengths = []
    weaknesses = []
    for category, score in scores.items():
        max_score = {'hook_concept': 15, 'structure_plot': 20, 'writing_quality': 20,
                     'consistency': 10, 'originality': 20, 'engagement': 10, 'compliance': 5}
        pct = score / max_score[category] * 100
        label = category.replace('_', ' ').title()
        if pct >= 80:
            strengths.append(label)
        elif pct < 60:
            weaknesses.append(label)

    return {
        'total_score': total,
        'scores': scores,
        'max_scores': {'hook_concept': 15, 'structure_plot': 20, 'writing_quality': 20,
                       'consistency': 10, 'originality': 20, 'engagement': 10, 'compliance': 5},
        'flags': flags,
        'strengths': strengths,
        'weaknesses': weaknesses,
        'similarity_warning': similarity_warning,
        'max_similarity': max_similarity,
        'passed': total >= APEX_PASS_SCORE and scores['writing_quality'] >= APEX_MIN_WRITING_QUALITY and not similarity_warning,
    }


def check_cooldown(user_id, db_session):
    """Check if user is on cooldown or exceeded submission limit."""
    from webapp.models import ApexSubmission

    cutoff_14d = datetime.utcnow() - timedelta(days=APEX_COOLDOWN_DAYS)
    recent_rejection = db_session.query(ApexSubmission).filter(
        ApexSubmission.user_id == user_id,
        ApexSubmission.status == 'rejected',
        ApexSubmission.created_at > cutoff_14d,
    ).first()

    if recent_rejection:
        days_left = APEX_COOLDOWN_DAYS - (datetime.utcnow() - recent_rejection.created_at).days
        return False, f"You were rejected {APEX_COOLDOWN_DAYS - days_left} days ago. Please wait {days_left} more day(s) before resubmitting."

    cutoff_60d = datetime.utcnow() - timedelta(days=60)
    submission_count = db_session.query(ApexSubmission).filter(
        ApexSubmission.user_id == user_id,
        ApexSubmission.created_at > cutoff_60d,
    ).count()

    if submission_count >= APEX_MAX_SUBMISSIONS_60_DAYS:
        return False, f"You've submitted {submission_count} times in 60 days (max {APEX_MAX_SUBMISSIONS_60_DAYS}). Please wait before resubmitting."

    return True, None
