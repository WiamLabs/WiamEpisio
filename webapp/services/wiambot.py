MIN_MESSAGE_LENGTH = 2


def handle_unmatched(user_message, user_id, db_session):
    """Save unmatched/failed message for review."""
    from webapp.models import BotUnmatchedMessage

    msg = BotUnmatchedMessage(
        user_message=user_message.strip()[:500],
        user_id=user_id,
    )
    db_session.add(msg)
    try:
        db_session.commit()
    except Exception:
        db_session.rollback()

    try:
        from webapp.services.platform_notify import notify_training_queue
        user_name = ''
        try:
            from webapp.models import User
            user_obj = db_session.query(User).filter_by(wiam_id=user_id).first()
            if not user_obj:
                user_obj = db_session.query(User).get(user_id)
            if user_obj:
                user_name = user_obj.display_name
        except Exception:
            pass
        notify_training_queue(user_message, user_id, user_name=user_name)
    except Exception as e:
        log.warning("Could not send training queue notification: %s", e)


def chat(user_message, user_id, db_session):
    """Main chat endpoint \u2014 pure AI responses via Groq > Cerebras > Gemini.
    No hardcoded patterns or knowledge base. All responses AI-generated."""
    text_stripped = user_message.strip()

    if len(text_stripped) < MIN_MESSAGE_LENGTH:
        return {
            'reply': "Hey! Type a question and I'll do my best to help you with anything WiamApp-related \U0001f60a",
            'intent': None,
            'confidence': 0,
            'matched': False,
        }

    # Check per-user daily limit
    try:
        from webapp.services.ai_service import (
            chat_completion, is_available, check_user_limit, increment_user_limit,
        )
        if not check_user_limit(user_id, 'chat'):
            return {
                'reply': (
                    "You've used all your AI messages for today! Your limit resets "
                    "at midnight UTC. In the meantime, feel free to browse stories "
                    "or check out what's trending \U0001f4da"
                ),
                'intent': 'rate_limited',
                'confidence': 100,
                'matched': True,
            }

        # AI response (Groq > Cerebras > Gemini)
        if is_available():
            ai_reply = chat_completion(
                SYSTEM_PROMPT, text_stripped,
                max_tokens=512, temperature=0.7,
            )
            if ai_reply:
                increment_user_limit(user_id, 'chat')
                return {
                    'reply': ai_reply,
                    'intent': 'ai_response',
                    'confidence': 95,
                    'matched': True,
                }
    except Exception as e:
        log.warning("AI chat failed: %s", e)

    # All AI providers offline — log and show offline message
    handle_unmatched(user_message, user_id, db_session)
    return {
        'reply': _OFFLINE_MESSAGE,
        'intent': None,
        'confidence': 0,
        'matched': False,
    }


