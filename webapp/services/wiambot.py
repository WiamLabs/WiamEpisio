def analyze_manuscript(text, user_id=None):
    """AI-powered manuscript feedback with heuristic fallback."""
    word_count = len(text.split())

    # Try AI analysis first (Gemini via creator_tool)
    try:
        from webapp.services.ai_service import creator_tool, check_user_limit, increment_user_limit
        can_use_ai = True
        if user_id:
            can_use_ai = check_user_limit(user_id, 'creator_tool')

        if can_use_ai:
            system_prompt = """You are a professional manuscript editor at WiamApp.

Analyze this manuscript excerpt and give actionable feedback.

Return a JSON object:
{
    "score": "Excellent" | "Good" | "Needs Work" | "Major Revisions Needed",
    "summary": "1-2 sentence overall assessment",
    "word_count": <number>,
    "issues": [
        {"issue": "short title", "detail": "specific explanation", "severity": "high|medium|low"}
    ],
    "strengths": ["specific thing done well"],
    "ai_feedback": "2-3 sentences of encouraging, specific editorial feedback"
}

Be honest but encouraging. Focus on craft: hook, pacing, dialogue, show-don't-tell, voice."""

            import json as _json
            ai_result = creator_tool(system_prompt, text[:4000], max_tokens=1024, temperature=0.4)
            if ai_result:
                try:
                    parsed = _json.loads(ai_result) if isinstance(ai_result, str) else ai_result
                    if isinstance(parsed, dict) and 'score' in parsed:
                        parsed.setdefault('word_count', word_count)
                        parsed.setdefault('paragraph_count', len([p for p in text.split('\n\n') if p.strip()]))
                        parsed.setdefault('sentence_count', len([s for s in re.split(r'[.!?]+', text) if s.strip()]))
                        parsed['ai_powered'] = True
                        if user_id:
                            increment_user_limit(user_id, 'creator_tool')
                        return parsed
                except (_json.JSONDecodeError, TypeError):
                    pass
    except Exception as e:
        log.warning("AI manuscript analysis unavailable: %s", e)

    # Heuristic fallback
    feedback = []
    paragraphs = [p.strip() for p in text.split('\n\n') if p.strip()]
    sentences = re.split(r'[.!?]+', text)
    sentences = [s.strip() for s in sentences if s.strip()]

    # Check word count
    if word_count < 1500:
        feedback.append({
            'issue': 'Too short',
            'detail': f'Only {word_count} words. Aim for at least 1,500 words per chapter.',
            'severity': 'high',
        })

    # Check for weak hooks (first sentence)
    if sentences:
        first = sentences[0].lower()
        weak_starts = ['my name is', 'hi my name', 'i woke up', 'it was a', 'once upon a time',
                       'this is the story', 'let me tell you', 'i am going to']
        for ws in weak_starts:
            if first.startswith(ws):
                feedback.append({
                    'issue': 'Weak opening hook',
                    'detail': f'Your story starts with "{first[:50]}..." — consider a stronger, more engaging opening.',
                    'severity': 'high',
                })
                break

    # Check for long paragraph blocks
    long_paras = [p for p in paragraphs if len(p.split()) > 150]
    if long_paras:
        feedback.append({
            'issue': 'Long paragraph blocks',
            'detail': f'{len(long_paras)} paragraph(s) exceed 150 words. Break them up for readability.',
            'severity': 'medium',
        })

    # Check for dialogue presence
    dialogue_count = text.count('"') + text.count('\u201c') + text.count('\u201d')
    if dialogue_count < 4 and word_count > 500:
        feedback.append({
            'issue': 'Lack of dialogue',
            'detail': 'Very little dialogue detected. Stories need character conversations to feel alive.',
            'severity': 'medium',
        })

    # Check sentence variation
    if len(sentences) > 10:
        lengths = [len(s.split()) for s in sentences]
        avg = sum(lengths) / len(lengths)
        short_count = sum(1 for l in lengths if l < 5)
        long_count = sum(1 for l in lengths if l > 30)
        if short_count > len(sentences) * 0.5:
            feedback.append({
                'issue': 'Choppy sentences',
                'detail': 'More than half your sentences are very short. Vary sentence length for better flow.',
                'severity': 'low',
            })
        if long_count > len(sentences) * 0.3:
            feedback.append({
                'issue': 'Run-on sentences',
                'detail': f'{long_count} sentences exceed 30 words. Consider breaking them up.',
                'severity': 'medium',
            })

    # Check for excessive formatting
    caps_words = re.findall(r'\b[A-Z]{3,}\b', text)
    if len(caps_words) > 10:
        feedback.append({
            'issue': 'Excessive caps/formatting',
            'detail': f'{len(caps_words)} ALL-CAPS words detected. Use sparingly for emphasis.',
            'severity': 'low',
        })

    # Check for missing stakes
    stakes_words = ['must', 'need', 'or else', 'before', 'deadline', 'lose', 'die',
                    'destroy', 'save', 'protect', 'risk', 'danger', 'threat']
    has_stakes = any(sw in text.lower() for sw in stakes_words)
    if not has_stakes and word_count > 500:
        feedback.append({
            'issue': 'Missing stakes',
            'detail': 'No clear stakes detected. What does the character stand to lose? Make it clear.',
            'severity': 'high',
        })

    # Overall score
    high_issues = sum(1 for f in feedback if f['severity'] == 'high')
    med_issues = sum(1 for f in feedback if f['severity'] == 'medium')
    if not feedback:
        score = 'Excellent'
        summary = 'Your manuscript looks strong! Consider submitting for Wiam Apex.'
    elif high_issues == 0:
        score = 'Good'
        summary = 'A few minor things to polish, but overall solid work.'
    elif high_issues <= 2:
        score = 'Needs Work'
        summary = 'Some important issues to address before publishing or submitting.'
    else:
        score = 'Major Revisions Needed'
        summary = 'Several critical issues found. Review the checklist carefully.'

    return {
        'score': score,
        'summary': summary,
        'word_count': word_count,
        'paragraph_count': len(paragraphs),
        'sentence_count': len(sentences),
        'issues': feedback,
    }


